def move(board):
    if DEBUG:
        for row in board:
            print("".join(row))

    any_move = False
    new_board = np.full_like(board, ".")
    for x, row in enumerate(board):
        for y, cell in enumerate(row):
            cell_move = False
            if cell == ">":
                if y < len(row) - 1 and board[x][y + 1] == ".":
                    new_board[x][y] = "."
                    new_board[x][y + 1] = ">"
                    cell_move = True
                elif y == len(row) - 1 and board[x][0] == ".":
                    new_board[x][y] = "."
                    new_board[x][0] = ">"
                    cell_move = True

            if not cell_move and new_board[x][y] == ".":
                new_board[x][y] = board[x][y]
            else:
                any_move = True

    board = new_board

    new_board = np.full_like(board, ".")
    for x, row in enumerate(board):
        for y, cell in enumerate(row):
            cell_move = False
            if cell == "v":
                if x < len(board) - 1 and board[x + 1][y] == ".":
                    new_board[x][y] = "."
                    new_board[x + 1][y] = "v"
                    cell_move = True
                elif x == len(board) - 1 and board[0][y] == ".":
                    new_board[x][y] = "."
                    new_board[0][y] = "v"
                    cell_move = True

            if not cell_move and new_board[x][y] == ".":
                new_board[x][y] = board[x][y]
            else:
                any_move = True

    board = new_board

    return any_move, board


